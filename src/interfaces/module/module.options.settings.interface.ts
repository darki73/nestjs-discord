/**
 * Interface ModuleOptionsSettingsInterface
 */
export interface ModuleOptionsSettingsInterface {

    /**
     * Discord bot authentication token
     * @type { string }
     */
    token: string;

    /**
     * Discord bot command prefix
     * @type { string }
     */
    prefix: string;

    /**
     * Discord bot owner id
     * @type { string }
     */
    owner_id: string;

}
